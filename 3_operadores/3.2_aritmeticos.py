'''

Los valores numericos son ademas el de una serie
de operadores aritmeticos y matematicos

'''

# Suma + 
a = 1 + 3

# Resta -
a = 4 - 1

# Multiplicacion 
a = 3 * 3

# Division
a = 3 / 3 # Siempre retorna un numero flotante (float)

# Negacion -<<numero>>
a = -3

# Exponente
a = 3 ** 3

# Division entera 
a = 5 // 10 # Si los dos son int retorna int

# Modulo -> Residuo
a = 5 % 10

'''

El orden de presedencia es: 
1. Exponente **
2. Negacion -<<numero>>
3. Division (todo tipo)
4. Multiplicacion
5. Suma y resta 

'''

# Podemos redondear con: 
a = round(3.1) # 3

